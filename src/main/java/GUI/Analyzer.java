    @SuppressWarnings("UnusedAssignment")
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String[] pcap = new String[]{"pcap"};

        jFileChooser1.setMultiSelectionEnabled(false);
        jFileChooser1.addChoosableFileFilter(new SimpleFileFilter(pcap, "Pcap File (*.pcap)"));
        int val = jFileChooser1.showOpenDialog(this);
        if (val == jFileChooser1.APPROVE_OPTION) { //kontrola otvorenia
            File file = jFileChooser1.getSelectedFile();

            jTextPane1.setText(file.getName()); // napise nazov suboru s ktorym aktualne pracuje
            jTextArea1.setText("");
            String tempPath = null;
            try {
                tempPath = file.getCanonicalPath(); //vrati cestu k suboru v String
            } catch (IOException ex) {
                Logger.getLogger(Analyzer.class.getName()).log(Level.SEVERE, null, ex);
            }
            path = tempPath;
            ScanAllFrame first = new ScanAllFrame(tempPath, -1); //zavola logicku cast
            int count = first.GetNumbersRamceSudajmi();

            String tmp = new String();
            String adress = new String();

            for (int i = 0; i < count - 1; i++) {
                tmp = tmp + first.VypisKonkretnyRamec(i) + "\n";

            }
            adress = adress + first.GetIpAdress();
            jTextArea1.setText(tmp + "\n" + "IP adresy vysielajúcich uzlov: \n" + adress + "\nAdresa uzla s najväčším počtom odvysielaných bajtov: \n" + first.getMaxIp()); //vypise vsetky ramce

            count = 0;
            tmp = null;
            adress = null;
            first.FreeMemory(); // vycisti zoznam ramcov !!! nezabudnut

            // communication  = first.DetectCommunicationPorts();
            check++;
        }
    }//GEN-LAST:event_jButton1ActionPerformed
