    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // jTextArea1.setText("Vsetky komunikacie: ");
        if (check != 0) {
            ScanAllFrame second = new ScanAllFrame(path, jComboBox1.getSelectedIndex());
            String tmp = new String(second.DetectCommunicationPorts());
            if (tmp.isEmpty()) {
                jTextArea1.setText("Neboli najdene komunikacie\n");
            } else {
                jTextArea1.setText(tmp);
                second.FreeMemory();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

